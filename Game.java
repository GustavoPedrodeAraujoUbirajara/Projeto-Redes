    private boolean shoot(Player shooter, Player target) {

        if (bulletsInChamber == 0) {
            System.out.println("O pente est√° vazio! Recarregando...\n");
            setupChamber();
        }

        bulletsInChamber--;

        // total de balas dispon√≠veis
        int totalBullets = realBullets + fakeBullets;

        // sorteio aleat√≥rio
        int roll = (int) (Math.random() * totalBullets);

        boolean hit;

        if (roll < realBullets) {
            // bala REAL
            hit = true;
            realBullets--;
        } else {
            // bala FALSA
            hit = false;
            fakeBullets--;
        }

        printGunGraphic(hit);

        if (hit) {
            System.out.println("\nüí• BANG! " + target.getName() + " perdeu 1 vida!\n");
            target.loseLife();

            System.out.println("Balas restantes no pente: " + bulletsInChamber + "\n");
            return false; // turno acabou

        } else {
            System.out.println("\nüòÖ CLICK! Bala falsa, nada aconteceu.\n");

            System.out.println("Balas restantes no pente: " + bulletsInChamber + "\n");

            // se atirou em si mesmo e era falsa, ganha outro turno
            if (shooter == target) {
                System.out.println("Voc√™ atirou em si mesmo mas a bala era falsa!");
                System.out.println("Pode agir novamente.\n");
                return true; // turno extra
            }
        }

        return false;
    }

